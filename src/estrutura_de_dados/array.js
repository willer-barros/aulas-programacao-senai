// 1. Funções Comuns em Arrays
// Inserção/Remoção:
// push() – adiciona no final.
// pop() – remove do final.
// unshift() – adiciona no início.
// shift() – remove do início.
// splice() – insere ou remove em qualquer posição. 
// Transformação e Filtragem:
// map() – cria um novo array com transformações.
// filter() – filtra elementos com base em uma condição.
// reduce() – reduz o array a um único valor. 
// Busca e Iteração:
// forEach() – executa uma função em cada elemento.
// find() – retorna o primeiro elemento que satisfaz uma condição.
// includes() – verifica se um valor está presente.
// indexOf() – retorna o índice de um elemento.